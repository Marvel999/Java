package ifelse;

public class SwitchCase {
    public static void main(String[] args) {
        int num= 35;
        switch (num){
            case 20 :
                System.out.println("20");
                break;
            case 30 :
                System.out.println("30");
                break;
            default:
                System.out.println("num is not 30 & 20");

        }
    }
}
